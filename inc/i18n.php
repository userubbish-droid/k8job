function __(string $key): string
{
    static $dict = null;
    if ($dict === null) {
        $dict = i18n_dictionary();
    }
    $lang = app_lang();
    if (!isset($dict[$key])) {
        return $key;
    }
    return $dict[$key][$lang] ?? $dict[$key]['zh'];
}

function __f(string $key, ...$args): string
{
    return sprintf(__($key), ...$args);
}
